// My Age
let myAge = 27;
// Early years will change
let earlyYears = 2;
// Math for early dog years
earlyYears = earlyYears * 10.5;
// Math for later dog years
laterYears = myAge - 2;
// Dog years accounted for by my later years
laterYears *= 4;
// Early years and later years combined
myAgeInDogYears = earlyYears + laterYears;
// Making my name lower case
const myName = "Ali".toLowerCase()
// Logging an interpolation
console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`);